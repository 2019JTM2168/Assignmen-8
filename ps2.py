def inp():
    global x, y
    x = int(input("Enter position : "))  # taking position from user
    if (x > 9 or x < 1):
        print("Invalid position")
        exit()
    y = int(input("Enter number : "))  # taking number from user
    if (y > 9 or y < 1):
        print("Invalid number")
        exit()
    return x, y


